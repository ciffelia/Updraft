#include "Math.hpp"

#include <math.h>

double Math::Pow(const double x, const double y)
{
  return pow(x, y);
}

double Math::Sqrt(const double x)
{
  return sqrt(x);
}

double Math::Sin(const double x)
{
  return sin(x);
}

double Math::Cos(const double x)
{
  return cos(x);
}

double Math::Tan(const double x)
{
  return tan(x);
}

double Math::Asin(const double x)
{
  return asin(x);
}

double Math::Acos(const double x)
{
  return acos(x);
}

double Math::Atan(const double x)
{
  return atan(x);
}

double Math::Atan2(const double y, const double x)
{
  return atan2(y, x);
}
